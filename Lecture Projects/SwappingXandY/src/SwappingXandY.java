
public class SwappingXandY {
	public static void main(String[] args) {
		int x = 3;
		int y = 5;
			System.out.println("x = " + x);
			System.out.println("y = " + y);
			System.out.println();
		int a = x;
			x = y;
			y = a;
			System.out.println("x = " + x);
			System.out.println("y = " + y);
	}

}
